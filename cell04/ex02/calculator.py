#!/usr/bin/env python3

first_inp = int(input("Give me the first number: "))
sec_inp = int(input("Give me the second number: "))
print("Thank you!")
print(f"{first_inp} + {sec_inp} = {first_inp + sec_inp}")
print(f"{first_inp} - {sec_inp} = {first_inp - sec_inp}")
print(f"{first_inp} / {sec_inp} = {int(first_inp / sec_inp)}")
print(f"{first_inp} * {sec_inp} = {first_inp * sec_inp}")